// Language Toggle Functionality
let currentLanguage = 'en'; // Default to English

const languageToggle = document.querySelector('.language-toggle');
const htmlElement = document.documentElement;

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLanguage);
});

languageToggle.addEventListener('click', () => {
    currentLanguage = currentLanguage === 'en' ? 'ar' : 'en';
    setLanguage(currentLanguage);
});

function setLanguage(lang) {
    // Update HTML attributes
    htmlElement.setAttribute('lang', lang);
    htmlElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    
    // Update all elements with language data attributes
    const elementsWithLangData = document.querySelectorAll('[data-en][data-ar]');
    
    elementsWithLangData.forEach(element => {
        const text = element.getAttribute(`data-${lang}`);
        if (text) {
            element.textContent = text;
        }
    });
    
    // Update form placeholders
    const inputsWithPlaceholders = document.querySelectorAll('[data-placeholder-en][data-placeholder-ar]');
    
    inputsWithPlaceholders.forEach(input => {
        const placeholder = input.getAttribute(`data-placeholder-${lang}`);
        if (placeholder) {
            input.setAttribute('placeholder', placeholder);
        }
    });
    
    // Update language toggle button text
    const toggleText = languageToggle.querySelector('span');
    if (toggleText) {
        toggleText.textContent = lang === 'en' ? 'العربية' : 'English';
    }
    
    // Store language preference
    localStorage.setItem('preferredLanguage', lang);
}

// Load saved language preference
const savedLanguage = localStorage.getItem('preferredLanguage');
if (savedLanguage && (savedLanguage === 'en' || savedLanguage === 'ar')) {
    currentLanguage = savedLanguage;
}

